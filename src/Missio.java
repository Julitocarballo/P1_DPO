public class Missio {
    private int target;
    private int quantity;

    public Missio(int target, int quantity) {
        this.target = target;
        this.quantity = quantity;
    }

    public int getTarget() {
        return target;
    }

    public void setTarget(int target) {
        this.target = target;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
}
